    private void CariDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CariDataActionPerformed
        String hasil2 = inputKode.getText();
        int hasil = Integer.parseInt(hasil2);
        
        pelanggan = pelangganctrl.findPelanggan(hasil);
        
        
        inputNama.setText(pelanggan.getNmPelanggan());
        inputUsername.setText(pelanggan.getUsername());
        inputEmail.setText(pelanggan.getEmailPelanggan());
        inputAlamat.setText(pelanggan.getAlmtPelanggan());
        inputNotelp.setText(pelanggan.getNotelpPelanggan());
        
        
    }//GEN-LAST:event_CariDataActionPerformed

    private void UpdateDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateDataActionPerformed
        String hasil2 = inputKode.getText();
        int hasil = Integer.parseInt(hasil2);
        
        pelanggan = pelangganctrl.findPelanggan(hasil);
        
        pelanggan.setNmPelanggan(inputNama.getText());
        pelanggan.setUsername(inputUsername.getText());
        pelanggan.setEmailPelanggan(inputEmail.getText());
        pelanggan.setAlmtPelanggan(inputAlamat.getText());
        pelanggan.setNotelpPelanggan(inputNotelp.getText());
        
        try {
            pelangganctrl.edit(pelanggan);
            JOptionPane.showMessageDialog(null, "Data Pelanggan Diperbaharui");
            inputKode.setText("");
            inputNama.setText("");
            inputUsername.setText("");
            inputEmail.setText("");
            inputAlamat.setText("");
            inputNotelp.setText("");
            
        } catch (Exception ex) {
            Logger.getLogger(customer_form.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }//GEN-LAST:event_UpdateDataActionPerformed

    private void HapusDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusDataActionPerformed
        int pilihan = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data ini?", "Hapus Data", JOptionPane.YES_NO_OPTION);
        
        if(pilihan==0){
            String hasil2 = inputKode.getText();
            int hasil = Integer.parseInt(hasil2);
            
            try {
                pelangganctrl.destroy(hasil);
                JOptionPane.showMessageDialog(null, "Data Pelanggan telah Dihapus");
                inputKode.setText("");
                inputNama.setText("");
                inputUsername.setText("");
                inputEmail.setText("");
                inputAlamat.setText("");
                inputNotelp.setText("");
                
            } catch (NonexistentEntityException ex) {
                Logger.getLogger(customer_form.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
        
    }//GEN-LAST:event_HapusDataActionPerformed

    
    
